import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");

function resolveCoreRepoRoot() {
  const candidates = [
    process.env.DRTS_CORE_REPO_PATH,
    path.resolve(repoRoot, "../drts-fleet-platform"),
  ].filter(Boolean);
  return candidates;
}

function withBanner(contents, relativeSourcePath) {
  return `// AUTO-GENERATED SNAPSHOT.\n// Source: drts-fleet-platform/${relativeSourcePath}\n// Lifecycle: docs/02-architecture/authority/rgp-003-contract-sync-lifecycle.md\n// Run \`npm run sync:contracts\` after core contract changes.\n// Do not edit this file directly.\n\n${contents}`;
}

async function main() {
  let coreRepoRoot = null;
  for (const candidate of resolveCoreRepoRoot()) {
    const contractsEntry = path.resolve(
      candidate,
      "packages/contracts/src/index.ts",
    );
    const platformCodesEntry = path.resolve(
      candidate,
      "packages/contracts/src/platform-codes.ts",
    );
    try {
      await fs.access(contractsEntry);
      await fs.access(platformCodesEntry);
      coreRepoRoot = candidate;
      break;
    } catch {
      // continue
    }
  }

  if (!coreRepoRoot) {
    throw new Error(
      "Unable to locate drts-fleet-platform. Set DRTS_CORE_REPO_PATH or keep the sibling checkout available.",
    );
  }

  const generatedDir = path.resolve(repoRoot, "src/lib/drts-shim/generated");
  await fs.mkdir(generatedDir, { recursive: true });
  const existingGeneratedFiles = (await fs.readdir(generatedDir)).filter(
    (fileName) => fileName.endsWith(".ts"),
  );
  await Promise.all(
    existingGeneratedFiles.map((fileName) =>
      fs.rm(path.join(generatedDir, fileName), { force: true }),
    ),
  );

  const sourceDir = path.resolve(coreRepoRoot, "packages/contracts/src");
  const fileNames = (await fs.readdir(sourceDir))
    .filter((fileName) => fileName.endsWith(".ts"))
    .sort();

  for (const fileName of fileNames) {
    const sourceRelative = `packages/contracts/src/${fileName}`;
    const source = path.resolve(coreRepoRoot, sourceRelative);
    const contents = await fs.readFile(source, "utf8");
    await fs.writeFile(
      path.join(generatedDir, fileName),
      withBanner(contents, sourceRelative),
      "utf8",
    );
  }

  process.stdout.write(
    `Synced DRTS contract snapshot from ${coreRepoRoot} into src/lib/drts-shim/generated/.\n`,
  );
}

await main();
